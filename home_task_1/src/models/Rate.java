package models;

public class Rate {
    private float turnoutRate;
    private float overTimeRate;
    private float vacationRate;
    private float sickDaysRate;
    private float businessTripRate;
    private float absenceRate;

    public float getTurnoutRate() {
        return turnoutRate;
    }

    public float getOverTimeRate() {
        return overTimeRate;
    }

    public float getVacationRate() {
        return vacationRate;
    }

    public float getSickDaysRate() {
        return sickDaysRate;
    }

    public float getBusinessTripRate() {
        return businessTripRate;
    }

    public float getAbsenceRate() {
        return absenceRate;
    }
}
